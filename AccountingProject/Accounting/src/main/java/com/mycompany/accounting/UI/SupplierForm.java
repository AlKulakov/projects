    private void wrongDataDelay(){
            //Подпись о неверном вводе
            resultLabel.setText("Введены неверные данные.");
            Timer timer = new Timer(4000, e-> {
            resultLabel.setText(" ");});
            timer.start();
    }
    
    private void wrongFieldDelay(javax.swing.JTextField field){
        //Подсветка неверно введенного поля
        if(!field.getBackground().equals(Color.red)){
            field.setBackground(Color.red);
            field.setForeground(Color.WHITE);
            Timer timer = new Timer(1000, e-> {
            field.setBackground(Color.WHITE);
            field.setForeground(Color.black);});
            timer.start();
        }
    }
    
    public boolean isNumeric(String number){
        //Проверка числовой строки
        try{
            Integer.parseInt(number);
            return true;
        } catch(NumberFormatException e){
            return false;
        }
    }
    
    private boolean dataConfirm(String name, String phone, String account, String address){
        //Проверка данных на соответствие БД
        boolean flag = true;
        if(name.trim().equals("")){
            wrongFieldDelay(nameField);
            flag = false;
        }
        if(phone.trim().equals("") || phone.length() < 11) {
            wrongFieldDelay(phoneField);
            flag = false;
        }
        if(account.trim().equals("") || account.length()<19){
            wrongFieldDelay(accountField);
            flag = false;
        }
        if(address.trim().equals("")){
            wrongFieldDelay(addressField);
            flag = false;
        }
        return flag;
    }
    private void accountFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_accountFieldKeyPressed
        //Ввод пробелов каждые четыре цифры. Для визуального комфорта
        String text = accountField.getText();
        int temp=0;
        
        if(text.length()==4 || text.length()==9 || text.length()==14){
            accountField.setText(text+" ");
            temp++;
        }
        
    }//GEN-LAST:event_accountFieldKeyPressed

    private void accountFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_accountFieldKeyTyped
        //Поле с вводом счёта позволяет вводить данные в формате карты. Максимальная длина - 19 символов.
        String text = accountField.getText();
        if(text.length()==19){
            evt.consume();
        }
        char c = evt.getKeyChar();
            if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)) {
                evt.consume();
            }
        // TODO add your handling code here:
    }//GEN-LAST:event_accountFieldKeyTyped

