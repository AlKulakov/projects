    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        //Изменить данные в БД при нажатии
        int id = 0;
        try{
            id = Integer.parseInt(idField.getText());
        } catch(NumberFormatException e){
            wrongFieldDelay(idField);
            wrongDataDelay();
        }
        String name = nameField.getText();
        String phone = phoneField.getText();
        String account = accountField.getText();
        String address = addressField.getText();
        String description = descriptionTextArea.getText();
        if(dataConfirm(name, phone, account, address))
        if(supplierQuery.editSupplier(id, name, phone, account, address, description)){
            resultLabel.setText("Оборудование изменено!");
            clearFields();
            refreshTable();
        }
        else {
            wrongDataDelay();
        }
        else {
            wrongDataDelay();
        }
    }//GEN-LAST:event_editButtonActionPerformed

