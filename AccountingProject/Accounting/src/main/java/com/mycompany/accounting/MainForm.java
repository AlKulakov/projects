    private void aboutMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aboutMenuItemActionPerformed
        if(aboutFormCount<1){
            AboutForm aboutForm = new AboutForm();
            aboutForm.setLocationRelativeTo(null);
            aboutForm.pack();
            aboutForm.show();
            aboutFormCount++;
        }
    }//GEN-LAST:event_aboutMenuItemActionPerformed
    private void refreshTable(JTable table, String type){
        //Обновление таблицы при нажатии кнопки
        table.setModel(new DefaultTableModel(null, new Object[]{type,"Описание", "Количество"}));
        accountingQuery.fillAccountingTable(table, type);
    }
