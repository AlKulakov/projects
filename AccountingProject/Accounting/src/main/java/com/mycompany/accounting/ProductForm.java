    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        //Добавление данных продуктов в таблицу по нажатии кнопки
        String name = nameField.getText();
        String description = descriptionTextArea.getText();
            if(dataConfirm(name))
                if(productQuery.addStuff(name, description, "Продукт")){
                    resultLabel.setText("Продукт добавлен!");
                    clearFields();
                    refreshTable();
                }
                else {
                    wrongDataDelay();
                }
            else {
                wrongDataDelay();
                wrongFieldDelay(nameField);
            }
    }//GEN-LAST:event_addButtonActionPerformed

