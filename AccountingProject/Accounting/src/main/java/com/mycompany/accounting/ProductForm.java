    private void clearFields(){
        //Очистка полей ввода
        idField.setText("");
        nameField.setText("");
        descriptionTextArea.setText("");
    }
    
    private void refreshTable(){
        //Обновление таблицы
        productTable.setModel(new DefaultTableModel(null, new Object[]{"ID","Название продукта","Описание"}));
        productQuery.fillStuffTable(productTable, "Продукт");
    }
    private void wrongDataDelay(){
        //Показать информацию о неверном вводе данных
            resultLabel.setText("Введены неверные данные.");
            Timer timer = new Timer(4000, e-> {
            resultLabel.setText(" ");});
            timer.start();
    }
    
    private void wrongFieldDelay(javax.swing.JTextField field){
        //Подсветить поле ввода с ошибочной информацией
        if(!field.getBackground().equals(Color.red)){
            field.setBackground(Color.red);
            field.setForeground(Color.WHITE);
            Timer timer = new Timer(1000, e-> {
            field.setBackground(Color.WHITE);
            field.setForeground(Color.black);});
            timer.start();
        }
    }
    
    public boolean isNumeric(String number){
        //Проверка, является ли строка числовой
        try{
            Integer.parseInt(number);
            return true;
        } catch(NumberFormatException e){
            return false;
        }
    }
    
    private boolean dataConfirm(String name){
    //Подтверждение корректности введенных данных
        if(!name.trim().equals(""))
            return true;
        else return false;
    }
    
