    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        //Запись в текстовые поля ввода информации из нажатой строки таблицы
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        int rowIndex = productTable.getSelectedRow();
        idField.setText(model.getValueAt(rowIndex, 0).toString());
        nameField.setText(model.getValueAt(rowIndex, 1).toString());
        descriptionTextArea.setText(model.getValueAt(rowIndex, 2).toString());
    }//GEN-LAST:event_productTableMouseClicked

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        
        //Редактирование информации в таблице по нажатии кнопки
        
        int id = 0;
        try{     
            id = Integer.parseInt(idField.getText());
        } catch(NumberFormatException e){
            wrongFieldDelay(idField);
            wrongDataDelay();
        }
        String name = nameField.getText();
        String description = descriptionTextArea.getText();
        if(dataConfirm(name))
                if(productQuery.editStuff(id, name, description)){
                    resultLabel.setText("Продукт изменен!");
                    clearFields();
                    refreshTable();
                }
                else {
                    wrongDataDelay();
                }
            else {
                wrongDataDelay();
                wrongFieldDelay(nameField);
            }
    }//GEN-LAST:event_editButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        //Удаление информации из БД при нажатии кнопки
        int id = 0;
        try{     
            id = Integer.parseInt(idField.getText());
        } catch(NumberFormatException e){
            wrongFieldDelay(idField);
            wrongDataDelay();
        }
        if(productQuery.removeStuff(id)){
            resultLabel.setText("Продукт удален!");
            clearFields();
            refreshTable();
        }
        else {
            wrongDataDelay();
            wrongFieldDelay(idField);
        }
    }//GEN-LAST:event_removeButtonActionPerformed

