    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed

        int id = 0;
        try{
            id = Integer.parseInt(idField.getText());
        } catch(NumberFormatException e){
            wrongFieldDelay(idField);
            wrongDataDelay();
            return;
        }
        String supplierName = supplierComboBox.getSelectedItem().toString();
        String stuffName = stuffComboBox.getSelectedItem().toString();
        int amount = 0;
        try{
            amount = Integer.parseInt(amountField.getText());
        } catch(Exception e){
            wrongDataDelay();
            wrongFieldDelay(amountField);
            return;
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        String date = new java.util.Date().toString();
        try{
            date = dateFormat.format(dateChooser.getDate());
        } catch(Exception e){
            wrongDataDelay();
            return;
        }
        if(dataConfirm(amount))
        if(supplies.editSupply(id, supplierName, stuffName, amount, date)){
            resultLabel.setText("Поставка изменена!");
            clearFields();
            refreshTable();
        }
        else {
            wrongDataDelay();
        }
        else {
            wrongDataDelay();
        }
    }//GEN-LAST:event_editButtonActionPerformed

