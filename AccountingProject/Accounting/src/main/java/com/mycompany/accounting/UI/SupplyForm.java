    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String supplierName = supplierComboBox.getSelectedItem().toString();
        String stuffName = stuffComboBox.getSelectedItem().toString();
        int amount = 0;
        try{
            amount = Integer.parseInt(amountField.getText());
        } catch(Exception e){
            wrongDataDelay();
            wrongFieldDelay(amountField);
            return;
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        String date = new Date().toString();
        try{
        date = dateFormat.format(dateChooser.getDate());
        } catch(Exception e){
            wrongDataDelay();
            return;
        }
        if(dataConfirm(amount))
        if(supplies.addSupply(supplierName, stuffName, amount, date)){
            resultLabel.setText("Поставка добавлена!");
            clearFields();
            refreshTable();
        }
        else {
            wrongDataDelay();
        }
        else {
            wrongDataDelay();
        }
    }//GEN-LAST:event_addButtonActionPerformed

