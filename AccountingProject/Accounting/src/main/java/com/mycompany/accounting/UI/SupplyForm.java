    private void suppliesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_suppliesTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) suppliesTable.getModel();
        int rowIndex = suppliesTable.getSelectedRow();
        idField.setText(model.getValueAt(rowIndex, 0).toString());
        supplierComboBox.setSelectedItem(model.getValueAt(rowIndex, 2).toString());
        if(((DefaultComboBoxModel)stuffComboBox.getModel()).getIndexOf(model.getValueAt(rowIndex, 1)) == -1){
            if(categoryComboBox.getSelectedItem().toString().equals("Продукт"))
                categoryComboBox.setSelectedItem("Оборудование");
            else
                categoryComboBox.setSelectedItem("Продукт");
            stuffComboBox.setSelectedItem(model.getValueAt(rowIndex, 1).toString());
        } else
            stuffComboBox.setSelectedItem(model.getValueAt(rowIndex, 1).toString());
        amountField.setText(model.getValueAt(rowIndex, 3).toString());
        Date date;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 4).toString());
            dateChooser.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(SupplyForm.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_suppliesTableMouseClicked

