    boolean RESIZE_S = false;
    boolean RESIZE_W = false;
    boolean RESIZE_E = false;
    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        x = evt.getX();
        y = evt.getY();
        if(x <=this.getWidth() && x >= this.getWidth()-5 && max_size == false){
            RESIZE = true;// TODO add your handling code here:
            RESIZE_E = true;// TODO add your handling code here:
            RESIZE_S = false;
            RESIZE_W = false;
        }
        else if(y <=this.getHeight() && y >= this.getHeight()-5 && max_size == false){
            RESIZE = true;
            RESIZE_S = true;
            RESIZE_W = false;
            RESIZE_E = false;
        }
        
        
        else RESIZE = false;
    }//GEN-LAST:event_formMousePressed

    private void formMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseDragged
                // TODO add your handling code here:
        if(RESIZE)
            if (!max_size){
                    if(RESIZE_E){
                        if(evt.getX()>500)
                            this.setSize(evt.getX(), this.getHeight());
                    }
                    else if(RESIZE_S){
                        if(evt.getY()>300)
                            this.setSize(this.getWidth(), evt.getY());
                    }
    }
    }//GEN-LAST:event_formMouseDragged

