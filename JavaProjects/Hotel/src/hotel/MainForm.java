    private void formMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseMoved
        x = evt.getX();
        y = evt.getY();
        if (!max_size && aboutFormCount==0)
            if(x <=this.getWidth() && x >= this.getWidth()-5){
                mainPanel.setCursor(new Cursor(E_RESIZE_CURSOR));
            }
            else if(y <=this.getHeight() && y >= this.getHeight()-5){
                mainPanel.setCursor(new Cursor(S_RESIZE_CURSOR));
            }
            
            else mainPanel.setCursor(new Cursor(DEFAULT_CURSOR));// TODO add your handling code here:
    }//GEN-LAST:event_formMouseMoved

