    private void extendMenuMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_extendMenuMousePressed
        extendMenu.setSelected(false);
        GraphicsEnvironment env = GraphicsEnvironment.getLocalGraphicsEnvironment();
        if(max_size==true){
            this.setExtendedState(NORMAL);
            max_size=false;
        }
        else{
        this.setMaximizedBounds(env.getMaximumWindowBounds());
        this.setExtendedState(MAXIMIZED_BOTH);
        max_size=true;
        }
        this.setLocationRelativeTo(null);// TODO add your handling code here:
    }//GEN-LAST:event_extendMenuMousePressed

